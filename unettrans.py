class Downsample_block_fuse(nn.Module):
    def __init__(self, in_channels, out_channels):
        super(Downsample_block_fuse, self).__init__()
        self.conv1 = nn.Conv2d(in_channels, out_channels, 3, padding=1) #3-->3*3conv
        self.bn1 = nn.BatchNorm2d(out_channels)
        #self.MKC = MKC(out_channels)
        self.conv2 = nn.Conv2d(out_channels, out_channels, 3, padding=1)
        self.bn2 = nn.BatchNorm2d(out_channels)
        self.skip4_2 = _NonLocalBlockND4(256)
    def forward(self,x,y):
        x_new = self.skip4_2(y,x)
        x_new = F.relu(self.bn1(self.conv1(x_new)))
        y_new = F.relu(self.bn2(self.conv2(x_new)))
        #print(x_new.shape)
        #y = F.relu(self.bn1(self.MKC(x)))
        x_new = F.max_pool2d(y_new, 2, stride=2)
        #print(x_new.shape)  
        return x_new, y_new
