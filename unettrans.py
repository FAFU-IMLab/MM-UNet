class bottleneck(nn.Module):
    def __init__(self, in_channels, out_channels):
       
        super(bottleneck, self).__init__()
        self.conv1_1 = nn.Conv2d(in_channels, 256, 3, padding=1) #3-->3*3conv
        self.bn1_1 = nn.BatchNorm2d(256)
        
        self.conv1_2 = nn.Conv2d(in_channels, 256, 3, padding=1) #3-->3*3conv
        self.bn1_2 = nn.BatchNorm2d(256)
        self.conv1_3 = nn.Conv2d(in_channels, 256, 3, padding=1) #3-->3*3conv
        self.bn1_3 = nn.BatchNorm2d(256)
        self.conv1_4 = nn.Conv2d(in_channels, 256, 3, padding=1) #3-->3*3conv
        self.bn1_4 = nn.BatchNorm2d(256)
        self.conv = nn.Conv2d(out_channels, out_channels, 3, padding=1) #3-->3*3conv
        self.bn = nn.BatchNorm2d(out_channels)
        self.bn_cat = nn.BatchNorm2d(out_channels)

        self.trskip1 = SwinTransformerBlock_One()
        self.trskip2 = SwinTransformerBlock_Two()
        self.trskip3 = SwinTransformerBlock_Three()
        self.trskip4 = SwinTransformerBlock_Four()
        

    def forward(self, x):
   
        x1 = F.relu(self.bn1_1(self.conv1_1(x)))
        x2 = F.relu(self.bn1_2(self.conv1_2(x)))
        x3 = F.relu(self.bn1_3(self.conv1_3(x)))
        x4 = F.relu(self.bn1_4(self.conv1_4(x)))
        
        attn1 = self.trskip1(x1)
        x1 = attn1[0]
        k1 = attn1[1]
        attn2 = self.trskip2(x2,k1)
        x2 = attn2[0]
        k2 = attn2[1]
        attn3 = self.trskip3(x3,k2)
        x3 = attn3[0]
        k3 = attn3[1]
        x4 = self.trskip4(x4,k3)
        #x1_2 = attn1_2[0]
        #k1_2 = attn1_2[1]

        x_two = torch.cat((x1,x2,x3,x4),dim=1)
        x_two = self.bn_cat(x_two)

        x = F.relu(self.bn(self.conv(x_two)))
        #print(x.size())
        #--------
        return x
class Downsample_block(nn.Module):
    def __init__(self, in_channels, out_channels):
        super(Downsample_block, self).__init__()
        self.conv1 = nn.Conv2d(in_channels, out_channels, 3, padding=1) #3-->3*3conv
        self.bn1 = nn.BatchNorm2d(out_channels)
        #self.MKC = MKC(out_channels)
        self.conv2 = nn.Conv2d(out_channels, out_channels, 3, padding=1)
        self.bn2 = nn.BatchNorm2d(out_channels)

    def forward(self, x):
        x = F.relu(self.bn1(self.conv1(x)))
        y = F.relu(self.bn2(self.conv2(x)))
        #y = F.relu(self.bn1(self.MKC(x)))

        x = F.max_pool2d(y, 2, stride=2)

        return x, y
