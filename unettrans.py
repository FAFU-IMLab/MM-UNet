class Upsample_block(nn.Module):
    def __init__(self, in_channels, out_channels):
        super(Upsample_block, self).__init__()
        self.transconv = nn.ConvTranspose2d(in_channels, out_channels, 4, padding=1, stride=2)
        self.conv1 = nn.Conv2d(in_channels, out_channels, 3, padding=1)
        self.bn1 = nn.BatchNorm2d(out_channels)
        #self.MKC = MKC(out_channels)
        self.conv2 = nn.Conv2d(out_channels, out_channels, 3, padding=1)
        self.bn2 = nn.BatchNorm2d(out_channels)

    def forward(self, x, y):
        x = self.transconv(x)
        x = torch.cat((x, y), dim=1)    #y指的是之前的feature map dim=1?---> N C H W  在C通道上进行的
        x = F.relu(self.bn1(self.conv1(x)))
        #x = F.relu(self.bn1(self.MKC(x)))
        x = F.relu(self.bn2(self.conv2(x)))
        
        return x



class _NonLocalBlockND_shift(nn.Module):
    """
    调用过程
    NONLocalBlock2D(in_channels=32),
    super(NONLocalBlock2D, self).__init__(in_channels,
            inter_channels=inter_channels,
            dimension=2, sub_sample=sub_sample,
            bn_layer=bn_layer)
    """
    def __init__(self,
                 in_channels,
                 inter_channels=None,
                 dimension=2,
                 sub_sample=True,
                 bn_layer=True):
        super(_NonLocalBlockND_shift, self).__init__()
 
        assert dimension in [1, 2, 3]
 
        self.dimension = dimension
        self.sub_sample = sub_sample
 
        self.in_channels = in_channels
        self.inter_channels = inter_channels
 
        if self.inter_channels is None:
            self.inter_channels = in_channels // 2
            # 进行压缩得到channel个数
            if self.inter_channels == 0:
                self.inter_channels = 1
        if dimension == 3:
            conv_nd = nn.Conv3d
            max_pool_layer = nn.MaxPool3d(kernel_size=(1, 2, 2))
            bn = nn.BatchNorm3d
        elif dimension == 2:
            conv_nd = nn.Conv2d
            max_pool_layer = nn.MaxPool2d(kernel_size=(2, 2))
            bn = nn.BatchNorm2d
        else:
            conv_nd = nn.Conv1d
            max_pool_layer = nn.MaxPool1d(kernel_size=(2))
            bn = nn.BatchNorm1d
 
        self.g = conv_nd(in_channels=self.in_channels,
                         out_channels=self.inter_channels,
                         kernel_size=1,
                         stride=1,
                         padding=0)
 
        if bn_layer:
            self.W = nn.Sequential(
                conv_nd(in_channels=self.inter_channels,
                        out_channels=self.in_channels,
                        kernel_size=1,
                        stride=1,
                        padding=0), bn(self.in_channels))
            nn.init.constant_(self.W[1].weight, 0)
            nn.init.constant_(self.W[1].bias, 0)
        else:
            self.W = conv_nd(in_channels=self.inter_channels,
                             out_channels=self.in_channels,
                             kernel_size=1,
                             stride=1,
                             padding=0)
            nn.init.constant_(self.W.weight, 0)
            nn.init.constant_(self.W.bias, 0)
 
        self.theta = conv_nd(in_channels=self.in_channels,
                             out_channels=self.inter_channels,
                             kernel_size=1,
                             stride=1,
                             padding=0)
        self.phi = conv_nd(in_channels=self.in_channels*2,
                           out_channels=self.inter_channels,
                           kernel_size=1,
                           stride=1,
                           padding=0)
 
        
 
    def forward(self, x,y4):
        '''
        :param x: (b, c,  h, w)
        :return:
        '''
        #print(x.shape) # 16 512 20 20
        batch_size,C,W,H = x.shape
        #print(self.g(x).shape) #16 256 10 10
        g_x = self.g(x).view(batch_size, self.inter_channels, -1)#[bs, c, w*h]

        g_x = g_x.permute(0, 2, 1)
        #print('g_x:',g_x.shape)
        theta_x = self.theta(x).view(batch_size, self.inter_channels, -1)
        theta_x = theta_x.permute(0, 2, 1)
        #print('theta_x:',theta_x.shape)
        phi_x = y4 #
        phi_x = self.phi(phi_x).view(batch_size, self.inter_channels, -1)
        #print('phi_x:',phi_x.shape)
        f = torch.matmul(theta_x, phi_x)
        #f = f.transpose(1,2)
        #print(f.shape) #16 400 100
        g_x = phi_x
        g_x = g_x.transpose(1,2)
        f_div_C = F.softmax(f, dim=-1)
        #print('f_div_C:',f_div_C.shape)
        #print('g_x:',g_x.shape)
        y = torch.matmul(f_div_C, g_x)
        y = y.permute(0, 2, 1).contiguous()
        #print(y.shape)
        y = y.view(batch_size, self.in_channels//2, *x.size()[2:])
        #print(y.shape) #16 256 20 20
        W_y = self.W(y) 
        #print(W_y.shape) #16 512 20 20
        z = W_y + x
        #print(z.shape) #16 512 20 20
        return z  

class _NonLocalBlockND4(nn.Module):
    def __init__(self, in_channels, inter_channels=None, sub_sample=False, bn_layer=True):
        """
        :param in_channels:
        :param inter_channels:
        :param dimension:
        :param sub_sample:
        :param bn_layer:
        """

        super(_NonLocalBlockND4, self).__init__()

        self.sub_sample = sub_sample

        self.in_channels = in_channels
        self.inter_channels = inter_channels

        if self.inter_channels is None:
            self.inter_channels = in_channels // 2
            if self.inter_channels == 0:
                self.inter_channels = 1


        max_pool_layer = nn.MaxPool2d(kernel_size=(2, 2))

        self.g1 = nn.Conv2d(in_channels=self.in_channels, out_channels=self.inter_channels,
                         kernel_size=1, stride=1, padding=0)
        self.theta1 = nn.Conv2d(in_channels=self.in_channels, out_channels=self.inter_channels,
                             kernel_size=1, stride=1, padding=0)
        self.phi1 = nn.Conv2d(in_channels=self.in_channels, out_channels=self.inter_channels,
                           kernel_size=1, stride=1, padding=0)

        self.g2 = nn.Conv2d(in_channels=self.in_channels*2, out_channels=self.inter_channels,
                         kernel_size=1, stride=1, padding=0)
        self.theta2 = nn.Conv2d(in_channels=self.in_channels*2, out_channels=self.inter_channels,
                             kernel_size=1, stride=1, padding=0)
        self.phi2 = nn.Conv2d(in_channels=self.in_channels*2, out_channels=self.inter_channels,
                           kernel_size=1, stride=1, padding=0)
        self.conv = nn.Conv2d(in_channels=400, out_channels=self.inter_channels,
                           kernel_size=1, stride=1, padding=0)
        if bn_layer:
            self.W = nn.Sequential(
                nn.Conv2d(in_channels=self.inter_channels, out_channels=self.in_channels,
                        kernel_size=1, stride=1, padding=0),
                nn.BatchNorm2d(self.in_channels)
            )
            nn.init.constant_(self.W[1].weight, 0)
            nn.init.constant_(self.W[1].bias, 0)
        else:
            self.W = nn.Conv2d(in_channels=self.inter_channels, out_channels=self.in_channels,
                             kernel_size=1, stride=1, padding=0)
            nn.init.constant_(self.W.weight, 0)
            nn.init.constant_(self.W.bias, 0)

        

        if sub_sample:
            self.g = nn.Sequential(self.g, max_pool_layer)
            self.phi = nn.Sequential(self.phi, max_pool_layer)

